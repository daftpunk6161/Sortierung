namespace Romulus.Tests.Benchmark.Generators;

/// <summary>
/// Generates a file with a non-ROM magic signature, typically used for negative controls.
/// Creates JFIF/PNG/PDF/MZ/ELF magic bytes in a ROM extension.
/// </summary>
internal sealed class NonRomContentGenerator : IStubGenerator
{
    public string GeneratorId => "non-rom-content";
    public string Extension => ".*";
    public IReadOnlyList<string> SupportedVariants { get; } = ["jfif", "png", "pdf", "mz", "elf", "mp3", "sfv", "nfo", "txt"];

    public byte[] Generate(string? variant = null, IReadOnlyDictionary<string, string>? parameters = null)
    {
        variant ??= "jfif";
        return variant switch
        {
            "jfif" => GenerateJfif(),
            "png" => GeneratePng(),
            "pdf" => GeneratePdf(),
            "mz" => GenerateMz(),
            "elf" => GenerateElf(),
            "mp3" => GenerateMp3(),
            "sfv" => GenerateSfv(),
            "nfo" => GenerateNfo(),
            "txt" => GenerateTxt(),
            _ => throw new ArgumentException($"Unknown variant '{variant}' for {GeneratorId}")
        };
    }

    private static byte[] GenerateJfif()
    {
        // JFIF header: FF D8 FF E0 + "JFIF\0"
        var data = new byte[32];
        data[0] = 0xFF; data[1] = 0xD8; data[2] = 0xFF; data[3] = 0xE0;
        data[4] = 0x00; data[5] = 0x10; // Length
        System.Text.Encoding.ASCII.GetBytes("JFIF").CopyTo(data, 6);
        return data;
    }

    private static byte[] GeneratePng()
    {
        // PNG: 89 50 4E 47 0D 0A 1A 0A
        return [0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
                0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00];
    }

    private static byte[] GeneratePdf()
    {
        // %PDF-1.4
        return System.Text.Encoding.ASCII.GetBytes("%PDF-1.4\n");
    }

    private static byte[] GenerateMz()
    {
        // MZ header (DOS executable)
        var data = new byte[64];
        data[0] = 0x4D; data[1] = 0x5A; // "MZ"
        return data;
    }

    private static byte[] GenerateElf()
    {
        // ELF header: 7F 45 4C 46
        var data = new byte[64];
        data[0] = 0x7F;
        System.Text.Encoding.ASCII.GetBytes("ELF").CopyTo(data, 1);
        return data;
    }

    private static byte[] GenerateMp3()
    {
        // MP3 ID3v2 header: 49 44 33
        var data = new byte[32];
        System.Text.Encoding.ASCII.GetBytes("ID3").CopyTo(data, 0);
        data[3] = 0x04; // Version 2.4
        return data;
    }

    private static byte[] GenerateSfv()
    {
        // SFV: plain text checksum file
        return System.Text.Encoding.ASCII.GetBytes("; Generated by WinSFV\nfile.nes ABCD1234\n");
    }

    private static byte[] GenerateNfo()
    {
        // NFO: plain text release info
        return System.Text.Encoding.ASCII.GetBytes("Release Info\n============\nGroup: TEST\n");
    }

    private static byte[] GenerateTxt()
    {
        // Plain text readme
        return System.Text.Encoding.ASCII.GetBytes("This is a readme file.\nNothing to see here.\n");
    }
}
